<h2 class="titletnc">ADVERTISING</h2>
<p>Colonel Sanders was a key component of KFC advertising until his death in 1980. Despite his death, Sanders remains a key icon of the company as an “international symbol of hospitality”. Early official slogans for the company included “North America’s Hospitality Dish” (from 1956) and “We fix Sunday dinner seven nights a week”. The “finger lickin’ good” slogan was used from 1956, and went on to become one of the best-known slogans of the 20th century. The trademark expired in the US in 2006. The first KFC logo was introduced in 1952 and featured a “Kentucky Fried Chicken” typeface and a logo of the Colonel. In 1962, Dave Thomas took Colonel Sanders’ Bucket and turned it into a sign that revolved in a circular motion in front of almost every KFC.</p>
<p>Advertising played a key role at KFC after it was sold by Sanders, and the company began to advertise on US television with a budget of US$4 million in 1966. In order to fund nationwide advertising campaigns, the Kentucky Fried Chicken Advertising Co-Op was established, giving franchisees ten votes and the company three when deciding on budgets and campaigns. In 1969, KFC hired its first national advertising agency, Leo Burnett. A notable Burnett campaign in 1972 was the “Get a bucket of chicken, have a barrel of fun” jingle, performed by Barry Manilow. By 1976 KFC was one of the largest advertisers in the US.
