<p>KFC (short for Kentucky Fried Chicken) is an American fast food restaurant chain headquartered in Louisville, Kentucky, that specializes in fried chicken. It is the world’s second-largest restaurant chain (as measured by sales) after McDonald’s, with 22,621 locations globally in 136 countries as of December 2018. The chain is a subsidiary of Yum! Brands, A Restaurant company that also owns the Pizza Hut, Taco Bell, and Wing Street chains.</p>
<p>KFC was founded by Colonel Harland Sanders, an entrepreneur who began selling fried chicken from his roadside restaurant in Corbin, Kentucky, during the Great Depression. Sanders identified the potential of the restaurant franchising concept, and the first “Kentucky Fried Chicken” franchise opened in Utah in 1952. KFC popularized chicken in the fast food industry, diversifying the market by challenging the established dominance of the hamburger. By branding himself as “Colonel Sanders”, Harland became a prominent figure of American cultural history, and his image remains widely used in KFC advertising to this day. However, the company’s rapid expansion overwhelmed the aging Sanders, and he sold it to a group of investors led by John Y. Brown Jr. and Jack C. Massey in 1964.</p>
<p>KFC’s original product is pressure-fried chicken pieces, seasoned with Sanders’ recipe of 11 herbs and spices. The constituents of the recipe represent a notable trade secret. Larger portions of fried chicken are served in a cardboard “bucket”, which has become a well-known feature of the chain since it was first introduced by franchisee Pete Harman in 1957. Since the early-1990s, KFC has expanded its menu to offer other chicken products such as chicken fillet sandwiches and wraps, as well as salads and side dishes such as French fries and coleslaw, desserts, and soft drinks; the latter often supplied by PepsiCo. KFC is known for its slogans “It’s Finger Lickin’ Good!”, “Nobody does chicken like KFC”, and “So good”.</p>
